def add_nums(a,b):
    """Returns the sum of a and b."""
    return a + b

def divide_nums(a,b):
    """Returns the division of a by b."""
    return a / b

def subtract_nums(a,b):
    """Returns the difference of a and b."""
    return a - b

def power(a,b):
    """Returns a raised to the power of b."""
    return a ** b

def multiply_nums(a,b):
    """Returns the product of a and b."""
    return a * b
